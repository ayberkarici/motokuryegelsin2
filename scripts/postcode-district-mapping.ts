// Posta kodunun ilk 3-4 hanesiyle ilçe eşleştir
export function getDistrictFromPostcode(postcode: string | undefined): string | null {
  if (!postcode) return null

  // Tam eşleşme dene
  if (postcodeToDistrict[postcode]) {
    return postcodeToDistrict[postcode]
  }

  // İlk 4 hane
  const firstFour = postcode.substring(0, 4)
  if (postcodeToDistrict[firstFour]) {
    return postcodeToDistrict[firstFour]
  }

  // İlk 3 hane
  const firstThree = postcode.substring(0, 3)
  for (const [key, district] of Object.entries(postcodeToDistrict)) {
    if (key.startsWith(firstThree)) {
      return district
    }
  }

  return null
}
